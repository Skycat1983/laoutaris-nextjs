import type { Metadata } from "next";
import { getServerSession } from "next-auth";
import React, { Suspense } from "react";
import Modal from "@/components/modules/modal/Modal";
import Footer from "@/components/modules/footer/Footer";
import {
  archivo,
  archivoBlack,
  cinzelDecorative,
  crimson,
  cormorant,
} from "@/lib/styles/fonts";
import "./globals.css";
import { authOptions } from "@/lib/config/authOptions";
import { Header } from "@/components/modules/navigation/header/Header";
import ClientContextBoundary from "@/contexts/ClientContextBoundary";

export const metadata: Metadata = {
  title: "Create Next App",
  description: "Generated by create next app",
};

// Separate component for session-dependent content
async function SessionLayout({ children }: { children: React.ReactNode }) {
  const session = await getServerSession(authOptions);

  return (
    <ClientContextBoundary session={session}>
      <Modal />
      <Header className="fixed top-0 left-0 right-0 z-50" />
      <main className="mt-[140px] sm:mt-[210px] md:mt-[220px] lg:mt-[160px] min-h-[calc(100vh-130px)] flex flex-col">
        {children}
      </main>
      <Footer />
    </ClientContextBoundary>
  );
}

export default function RootLayout({
  children,
}: {
  children: React.ReactNode;
}) {
  return (
    <html
      lang="en"
      className={`${archivoBlack.variable} ${archivo.variable} ${cinzelDecorative.variable} ${crimson.variable} ${cormorant.variable}`}
    >
      <head>
        <link rel="icon" href="/favicon.ico" sizes="any" />
      </head>
      <body className="bg-whitish">
        <Suspense
          fallback={
            <div className="mt-[140px] sm:mt-[210px] md:mt-[220px] lg:mt-[160px] min-h-[calc(100vh-130px)] flex flex-col">
              Loading...
            </div>
          }
        >
          <SessionLayout>{children}</SessionLayout>
        </Suspense>
      </body>
    </html>
  );
}
