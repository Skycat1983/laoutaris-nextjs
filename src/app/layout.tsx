import type { Metadata } from "next";
import { getServerSession } from "next-auth";
import React from "react";
import { GlobalFeaturesProvider } from "@/contexts/GlobalFeaturesContext";
import { SessionContextProvider } from "@/contexts/SessionProvider";
import Modal from "@/components/ui/modal/Modal";
import Footer from "@/components/ui/footer/Footer";
import {
  archivo,
  archivoBlack,
  cinzelDecorative,
  crimson,
  cormorant,
} from "@/lib/styles/fonts";
import "./globals.css";
import { authOptions } from "@/lib/config/authOptions";
import Header from "@/components/ui/header/header";

export const metadata: Metadata = {
  title: "Create Next App",
  description: "Generated by create next app",
};

export default async function RootLayout({
  children,
}: {
  children: React.ReactNode;
}) {
  const session = await getServerSession(authOptions);

  return (
    <html
      lang="en"
      className={`${archivoBlack.variable} ${archivo.variable} ${cinzelDecorative.variable} ${crimson.variable} ${cormorant.variable}`}
    >
      <head>
        <link rel="icon" href="/favicon.ico" sizes="any" />
      </head>
      <body className="bg-whitish">
        <SessionContextProvider session={session}>
          <GlobalFeaturesProvider>
            <Modal />
            <Header className="fixed top-0 left-0 right-0 z-50" />
            <main className="mt-[140px] sm:mt-[210px] md:mt-[220px] lg:mt-[160px] min-h-[calc(100vh-130px)] flex flex-col">
              {children}
            </main>
            <Footer />
          </GlobalFeaturesProvider>
        </SessionContextProvider>
      </body>
    </html>
  );
}
